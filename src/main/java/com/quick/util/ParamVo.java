public class ParamVo {
    private String url;
    private String html;

    @Override
    public String toString() {
        return "ParamVo{" +
                "url='" + url + '\'' +
                ", html='" + html + '\'' +
                '}';
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getHtml() {
        return html;
    }

    public void setHtml(String html) {
        this.html = html;
    }
}
